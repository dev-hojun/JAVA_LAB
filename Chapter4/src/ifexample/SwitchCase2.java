package ifexample;

public class SwitchCase2 {

	public static void main(String[] args) {
		int rank = 1;
		char medalColor;
		
		switch(rank) {
			case 1: medalColor = 'G';
					break; // break를 써주지 않으면 만족하더라고 빠져나오지 못하고 다음 case를 수행
			case 2: medalColor = 'S';
					break;
			case 3: medalColor = 'B';
					break;
			default : medalColor = 'A';
		}
		System.out.println(rank + "등 메달의 색깔은 " + medalColor + "입니다.");
	}
}
